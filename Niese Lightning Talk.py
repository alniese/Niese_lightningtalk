def oz_to_cups(ounces):
    cups = ounces*0.125 
    print ("there are", cups, "cups in", ounces, "ounces")
    
oz_to_cups(14)

def grams_to_cups(grams):
    cups = float(grams/220)
    print ("there are",cups,"cups in", grams,"grams")

grams_to_cups(483)

